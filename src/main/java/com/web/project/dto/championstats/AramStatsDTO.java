package com.web.project.dto.championstats;

public class AramStatsDTO {
    private int wins;
    private int losses;
    private int picks;
    private int kills;
    private int deaths;
    private int assists;
    private int goldEarned;
    private int cs;
    private double kda;
    private String tier;
    private int tierScore;
    private double winrate;
    private double pickrate;
    
	public int getWins() {
		return wins;
	}
	public void setWins(int wins) {
		this.wins = wins;
	}
	public int getLosses() {
		return losses;
	}
	public void setLosses(int losses) {
		this.losses = losses;
	}
	public int getPicks() {
		return picks;
	}
	public void setPicks(int picks) {
		this.picks = picks;
	}
	public int getKills() {
		return kills;
	}
	public void setKills(int kills) {
		this.kills = kills;
	}
	public int getDeaths() {
		return deaths;
	}
	public void setDeaths(int deaths) {
		this.deaths = deaths;
	}
	public int getAssists() {
		return assists;
	}
	public void setAssists(int assists) {
		this.assists = assists;
	}
	public int getGoldEarned() {
		return goldEarned;
	}
	public void setGoldEarned(int goldEarned) {
		this.goldEarned = goldEarned;
	}
	public int getCs() {
		return cs;
	}
	public void setCs(int cs) {
		this.cs = cs;
	}
	public double getKda() {
		return kda;
	}
	public void setKda(double kda) {
		this.kda = kda;
	}
	public String getTier() {
		return tier;
	}
	public void setTier(String tier) {
		this.tier = tier;
	}
	public int getTierScore() {
		return tierScore;
	}
	public void setTierScore(int tierScore) {
		this.tierScore = tierScore;
	}
	public double getWinrate() {
		return winrate;
	}
	public void setWinrate(double winrate) {
		this.winrate = winrate;
	}
	public double getPickrate() {
		return pickrate;
	}
	public void setPickrate(double pickrate) {
		this.pickrate = pickrate;
	}

}
